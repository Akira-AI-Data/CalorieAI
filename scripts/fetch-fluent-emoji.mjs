// Fetches Microsoft Fluent Emoji 3D PNGs for every emoji used in src/
// Writes to public/icons/fluent/<slug>.png and emits emoji-icon-map.ts
import { mkdirSync, writeFileSync, existsSync, statSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { execSync } from 'node:child_process';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const OUT_DIR = join(ROOT, 'public', 'icons', 'fluent');
const MAP_OUT = join(ROOT, 'src', 'data', 'emoji-icon-map.ts');
const ICON_PUBLIC_PATH = '/icons/fluent';

mkdirSync(OUT_DIR, { recursive: true });

// Collect unique emojis from src/
function collectEmojis() {
  const rg = execSync(
    `grep -rhoE "emoji: '[^']+'|emoji:\\"[^\\"]+\\"" ${join(ROOT, 'src')}`,
    { encoding: 'utf8' }
  );
  const set = new Set();
  for (const line of rg.split('\n')) {
    const m = line.match(/['"]([^'"]+)['"]/);
    if (m) set.add(m[1]);
  }
  // Extras found in other render sites (food-group, UI)
  ['🌾', '🍎', '🍗', '🥛', '🫑'].forEach(e => set.add(e));
  return [...set].sort();
}

// Normalize emoji → codepoint string matching fluent-emoji unicode field
// Fluent metadata uses lowercase hex, space-separated, with "fe0f" included
function emojiToCodepoints(e) {
  return [...e].map(c => c.codePointAt(0).toString(16)).join(' ');
}

async function fetchJSON(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return res.json();
}

async function main() {
  const emojis = collectEmojis();
  console.log(`Found ${emojis.length} unique emojis`);

  // 1. Get tree (all metadata.json paths)
  console.log('Fetching fluentui-emoji tree...');
  const tree = await fetchJSON(
    'https://api.github.com/repos/microsoft/fluentui-emoji/git/trees/main?recursive=1'
  );
  const metaPaths = tree.tree
    .filter(n => n.path.endsWith('/metadata.json'))
    .map(n => n.path);
  console.log(`  ${metaPaths.length} metadata.json files`);

  // 2. Parallel fetch metadata (8-wide) — each has glyph + unicode
  console.log('Fetching metadata (this may take ~2 min)...');
  const glyphToFolder = new Map();
  const unicodeToFolder = new Map();
  const batchSize = 8;
  let done = 0;
  for (let i = 0; i < metaPaths.length; i += batchSize) {
    const batch = metaPaths.slice(i, i + batchSize);
    await Promise.all(
      batch.map(async (path) => {
        try {
          const url = `https://raw.githubusercontent.com/microsoft/fluentui-emoji/main/${path}`;
          const meta = await fetchJSON(url);
          const folder = path.replace(/\/metadata\.json$/, '');
          if (meta.glyph) glyphToFolder.set(meta.glyph, folder);
          if (meta.unicode) unicodeToFolder.set(meta.unicode.toLowerCase(), folder);
        } catch (e) {
          // skip
        }
      })
    );
    done += batch.length;
    if (done % 80 === 0) process.stdout.write(`  ${done}/${metaPaths.length}\n`);
  }
  console.log(`  Parsed ${glyphToFolder.size} glyphs, ${unicodeToFolder.size} unicode entries`);

  // 3. Resolve each target emoji to folder
  const map = {};
  const failed = [];
  for (const emoji of emojis) {
    let folder = glyphToFolder.get(emoji);
    if (!folder) {
      const cp = emojiToCodepoints(emoji);
      folder = unicodeToFolder.get(cp);
    }
    if (!folder) {
      // Strip variation selector and retry
      const stripped = emoji.replace(/\uFE0F/g, '');
      folder = glyphToFolder.get(stripped);
      if (!folder) {
        const cpS = emojiToCodepoints(stripped);
        folder = unicodeToFolder.get(cpS);
      }
    }
    if (!folder) { failed.push(emoji); continue; }

    // Build 3D png path. Some emojis have Default/3D, others 3D directly.
    const folderName = folder.split('/').pop();
    const fileBase = folderName.toLowerCase().replace(/[ -]/g, '_').replace(/[^a-z0-9_]/g, '');
    const candidates = [
      `${folder}/3D/${fileBase}_3d.png`,
      `${folder}/Default/3D/${fileBase}_3d.png`,
    ];

    let downloaded = null;
    for (const cand of candidates) {
      const url = `https://raw.githubusercontent.com/microsoft/fluentui-emoji/main/${encodeURI(cand)}`;
      try {
        const res = await fetch(url);
        if (!res.ok) continue;
        const buf = Buffer.from(await res.arrayBuffer());
        if (buf.length < 500) continue;
        const slug = fileBase;
        const outFile = join(OUT_DIR, `${slug}.png`);
        writeFileSync(outFile, buf);
        downloaded = slug;
        break;
      } catch (e) {}
    }
    if (downloaded) {
      map[emoji] = `${ICON_PUBLIC_PATH}/${downloaded}.png`;
      process.stdout.write(`OK ${emoji} -> ${downloaded}\n`);
    } else {
      failed.push(emoji);
      process.stdout.write(`FAIL ${emoji} (folder: ${folder})\n`);
    }
  }

  const ts = `// Generated by scripts/fetch-fluent-emoji.mjs — do not edit by hand
// Icons from Microsoft Fluent Emoji (https://github.com/microsoft/fluentui-emoji). MIT licensed.
export const EMOJI_TO_ICON: Record<string, string> = ${JSON.stringify(map, null, 2)};
`;
  writeFileSync(MAP_OUT, ts);
  console.log(`\n${Object.keys(map).length}/${emojis.length} mapped.`);
  if (failed.length) console.log(`Failed: ${failed.join(' ')}`);
}

main().catch(e => { console.error(e); process.exit(1); });
